
/*
    FOR

    For significa "Para cada", e sua função é executar uma determinada tarefa para cada 
    ciclo que o programa der sobre a condição especificada nele.

    Sintaxe:

    -   for(let variável; condição de loop; expressão final){
        
        }
*/

for(let number; number < 10; number++){
    console.log(number + " x " + (number + 1) + " = " + (number * (number + 1))) 
}

