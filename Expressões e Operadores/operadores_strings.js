
// Concatenação de duas strings => Retorna uma união delas

const alpha = "alpha"
console.log(alpha + "bet")        // Retorna "alphabet"
console.log(alpha += "bet")      // Retorna "alphabet"
console.log(alpha + "bet")      // Retorna "alphabet"

// Se tentar fazer isso com um número, ele se torna uma string

console.log(alpha + 10010)      // Retorna "alpha10010"
