let x

console.log(x)

// Assignment ou Atribuição

x = 1

// Addition assignment ou Atribuição adicional

x += 2      // É o mesmo que x = x + 2

// Subtraction assignment

x -= 2      // É o mesmo que x = x - 2

// Multiplication assignment

x *= 2      // É o mesmo que x = x * 2

// Division assignment

x /= 2      // É o mesmo que x = x / 2

// Remainder assignment ou Atribuição de resto

x %= 2      // É o mesmo que x = x % 2

// Exponentiation assignment

x **= 2     // É o mesmo que x = x ** 2

