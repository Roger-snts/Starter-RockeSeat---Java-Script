
// Operadores Aritméticos

// Básicos
    // Multiplicação    => *
    console.log(5 * 5)

    // Divisão          => /
    console.log(9 / 1.3)

    // Soma             => +
    console.log(10 + 2)

    // Subtração        => -
    console.log(8 - 3)

// Especiais
    // Resto da divisão => %
        // Também chamado de Remainder/Resto
        let remainder
        remainder = 11 % 3
        console.log(remainder)

    // Incremento       => ++
        // Tem uma diferença entre colocar o operador antes ou após
        let increment = 0
        console.log(increment++) // Retorna 0, porque ele exibe e depois incrementa
        console.log(increment)   // Retorna 1
        console.log(++increment) // Retorna 2, porque primeiro incrementou, depois exibiu

    // Decremento       => --
        // Assim como o incremento, o decremento também tem diferença de posição
        let decrement = 0
        console.log(decrement--) // Retorna 0 e depois subtrai 1
        console.log(decrement)   // Retorna -1
        console.log(--decrement) // Subtrai 1 e retorna, originando um -2

    // Exponencial      => **
    console.log(3 ** 2)

