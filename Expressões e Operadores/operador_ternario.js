
/*
    Operador Ternário

    Para facilitar a comparação condicional e retorno de resultados, 
    existe o operador ternário

    Ele opera comparando dois valores. Se a resposta for verdadeira um 
    código executa, senão o outro executa.

    Defini-se por:

    -   condição ? true : false
*/

let age = 15
const canDrive = age >= 18 ? "can drive" : "can't drive";

console.log(canDrive)
